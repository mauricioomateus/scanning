!/bin/bash

echo " *** In progres... *** "

gobuster dns -d $1 -w $2 -q > $3

echo " =====================================================================  "
echo " ==================== Ajustando arquivos ... =========================  "
echo " ========================== .. 10% .. ================================  "

cat $3 | grep $1 | cut -d " " -f 2 > ip.txt

echo " =====================================================================  "
echo " ==================== Convertendo em IP  ... =========================  "
echo " ========================== .. 40% .. ================================  "

rm $3
nmap -v -sn -iL ip.txt -oG ativos.txt
rm ip.txt
clear

echo " =====================================================================  "
echo " ====================== Limpeza de tela .... =========================  "
echo " ========================== .. 70% .. ================================  "

cat ativos.txt | grep "Up" | cut -d " " -f 2 > ip_ativo.txt

rm ativos.txt

echo " =====================================================================  "
echo " ========================== Finalizado ===============================  "
echo " ========================== .. 100% .. ===============================  "
echo " ================== Arquivo criado: ip_ativos.txt ====================  "
